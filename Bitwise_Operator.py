print(25&17)            # & { bitwise And operator }
print(25|17)            # | { bitwise Or operator }
print(25^17)            # ^ { bitwise Xor operator }
print(~7)               # ~ { bitwise Not operator (positive number) it will saved as 2s compliment }
print(~-15)             # ~- { bitwise Not operator (negative number) it will saved as 2s compliment }
print(8<<2)             # << { bitwise right shift operator }
print(8>>2)             # >> { bitwise left shift operator }
